package ForLoopLab;

import java.util.Scanner;

public class Demo {
    public static void main(String[] args) {
        Scanner scanner = new Scanner(System.in);

        int number = 10;

        System.out.println(++number); //11
        System.out.println(number++); //11 + 1
        System.out.println(number);   //12

    }
}
