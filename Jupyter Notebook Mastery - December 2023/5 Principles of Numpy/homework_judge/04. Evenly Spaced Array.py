import numpy as np

# Assuming we have the variables start, stop, and N
# For demonstration, let's take start=1, stop=10, N=5 as in the first example
start = 1
stop = 10
N = 5

# Generating the array
evenly_spaced_array = np.linspace(start, stop, N)

# Printing the array
print(evenly_spaced_array)
