// Prefix increment
int a = 1;
Console.WriteLine(++a);
Console.WriteLine(a);
Console.WriteLine("---------------------");
// Postfix increment

int a2 = 1;
Console.WriteLine(a2++);
Console.WriteLine(a2);
Console.WriteLine("---------------------");
// Prefix decrement

int b = 1;
Console.WriteLine(--b);
Console.WriteLine(b);
Console.WriteLine("---------------------");

// Postfix decrement

int b2 = 1;
Console.WriteLine(b2--);
Console.WriteLine(b2);
Console.WriteLine("-----------------------");
